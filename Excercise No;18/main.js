"use strict";
let places = ["Makkah", "Turkey", "India", "Phalistine", "Dubai"];
//console our original Array!
console.log(" In original form :", places);
//print the array in alphabatical order without modifying the original array!
console.log(" In Alphabatical order:", [...places].sort());
//print the original array!
console.log(" Still in original form :", places);
//print array in reverse alphabatical order!
console.log(" Revers Alphabatical order:", [...places].reverse());
//still in original form !
console.log(" array is still in original form:", places);
//reverse the order of original array!
console.log("reverse the order of original array:", places.reverse());
//again reverse the order of list to print in original form!
console.log("Again reverse the order of array:", places.reverse());
//sort original array in alphabatical order!
console.log("sort original array in alphabatical order:", places.sort());
//sort in reverse alphabatical order !
console.log("sort in reverse alphabatical order:", places.reverse());
